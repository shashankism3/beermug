// Reusable function to render cards onto screen
function displayTrains(trainsList) {
    const gridContainer = document.getElementById("trains-grid");
    if (!gridContainer) return;
    
    gridContainer.innerHTML = "";

    if (trainsList.length === 0) {
        gridContainer.innerHTML = `<p style="grid-column: 1/-1; text-align: center; font-size: 1.2rem; color: #666; padding: 40px 0;">No matching Amrit Bharat trains found. Try searching another station or number.</p>`;
        return;
    }

    trainsList.forEach(train => {
        const cardHTML = `
            <a href="${train.link}">
                <div class="card">
                    <h3>${train.number}</h3>
                    <h4>${train.name}</h4>  
                    <p>Inaugurated on: ${train.inauguration}</p>    
                </div>
            </a>
        `;
        gridContainer.insertAdjacentHTML('beforeend', cardHTML);
    });
}

// Global initialization function
function initTrainEngine() {
    // Initial display load
    displayTrains(amritBharatTrains);

    // Dynamic filtering interface logic
    const searchBar = document.getElementById("search-input");
    if (searchBar) {
        searchBar.addEventListener("input", (e) => {
            const query = e.target.value.toLowerCase().trim();
            
            const filteredTrains = amritBharatTrains.filter(train => {
                return train.number.includes(query) || 
                       train.name.toLowerCase().includes(query) || 
                       (train.stations && train.stations.includes(query));
            });
            
            displayTrains(filteredTrains);
        });
    }
}

// Run engine safely when DOM structure maps are fully ready
document.addEventListener("DOMContentLoaded", initTrainEngine);